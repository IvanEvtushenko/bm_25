"""Токенизатор русского текста для BM25.

Что такое токенизация: «Иван купил 5 яблок.» → ["иван", "купить", "5", "яблоко"].
То есть строка → список значимых слов в нормальной форме.

Конвейер для одной строки текста (метод Tokenizer.__call__):
  1. razdel.tokenize  — режет строку на токены. Это умнее, чем text.split():
                         razdel понимает, что «И.А. Иванов» — три токена,
                         «5,5%» — три токена, «кавычки «такие»» — корректно
                         отделяет от слова. Лицензия MIT, чистый Python.
  2. .lower()         — единый регистр, чтобы «Банк» и «банк» считались одним.
  3. PUNCT-фильтр     — выкидываем знаки препинания, которые razdel мог
                         вернуть как отдельные токены.
  4. TOKEN_RE-фильтр  — белый список: оставляем только буквенно-цифровые
                         токены (отрезаем URL-обрывки, странные склейки).
  5. min_len-фильтр   — однобуквенные токены — обычно мусор от опечаток.
  6. pymorphy3        — лемматизация: «инфляционные» → «инфляционный»,
                         «банков» → «банк». Для русского без этого никак:
                         в BM25 «инфляция» и «инфляции» считались бы
                         разными терминами, и индекс плыл бы.
  7. стоп-слова       — после лемматизации выкидываем «и», «не», «который»
                         и пр. (RU_STOPWORDS).

На выходе — list[str] лемм.
"""
from __future__ import annotations

import re
import string
from dataclasses import dataclass, field

# razdel — библиотека из проекта Natasha (https://github.com/natasha/razdel).
# tokenize(str) -> Iterable[Substring], где Substring имеет .text/.start/.stop.
# Нам нужен только .text. Импортируем под алиас, чтобы имя tokenize не
# конфликтовало с возможными другими в коде.
from razdel import tokenize as razdel_tokenize

# pymorphy3 — морфологический анализатор (форк pymorphy2 без LGPL-словаря,
# https://github.com/no-plagiarism/pymorphy3). MorphAnalyzer тяжёлый
# (загружает словарь OpenCorpora ~30 МБ), создаём один экземпляр на процесс.
import pymorphy3


# Знаки препинания, которые razdel может вернуть как самостоятельные токены.
# string.punctuation — английская пунктуация (!"#$%&'()...). Дополняем
# типографскими русскими символами: лапки, тире, многоточие.
PUNCT = set(string.punctuation + "«»—–…„""''")

# Регулярка-белый список: токен должен состоять ТОЛЬКО из букв (любых,
# благодаря \w + явное русское), цифр, подчёркиваний и дефиса.
# `^...$` гарантирует, что вся строка целиком этому соответствует.
# Зачем: razdel может вернуть склейку вроде "5%" или "abc.def" — мы их пропускаем.
TOKEN_RE = re.compile(r"^[\wа-яА-ЯёЁ\-]+$", re.UNICODE)

# Базовый список русских стоп-слов: предлоги, союзы, частицы, местоимения,
# числительные. Эти слова встречаются почти в каждом документе, поэтому
# у них в BM25 близкий к нулю IDF — но всё равно занимают место в индексе
# и могут шумить. Лучше выкинуть.
# Список можно расширить доменными словами (см. README → "доменные стоп-слова").
RU_STOPWORDS = {
    "и", "в", "во", "не", "что", "он", "на", "я", "с", "со", "как", "а", "то",
    "все", "она", "так", "его", "но", "да", "ты", "к", "у", "же", "вы", "за",
    "бы", "по", "только", "ее", "мне", "было", "вот", "от", "меня", "еще",
    "нет", "о", "из", "ему", "теперь", "когда", "даже", "ну", "вдруг", "ли",
    "если", "уже", "или", "ни", "быть", "был", "него", "до", "вас", "нибудь",
    "опять", "уж", "вам", "ведь", "там", "потом", "себя", "ничего", "ей",
    "может", "они", "тут", "где", "есть", "надо", "ней", "для", "мы", "тебя",
    "их", "чем", "была", "сам", "чтоб", "без", "будто", "чего", "раз", "тоже",
    "себе", "под", "будет", "ж", "тогда", "кто", "этот", "того", "потому",
    "этого", "какой", "совсем", "ним", "здесь", "этом", "один", "почти",
    "мой", "тем", "чтобы", "нее", "сейчас", "были", "куда", "зачем", "всех",
    "никогда", "можно", "при", "наконец", "два", "об", "другой", "хоть",
    "после", "над", "больше", "тот", "через", "эти", "нас", "про", "всего",
    "них", "какая", "много", "разве", "три", "эту", "моя", "впрочем", "хорошо",
    "свою", "этой", "перед", "иногда", "лучше", "чуть", "том", "нельзя",
    "такой", "им", "более", "всегда", "конечно", "всю", "между",
    "это", "также", "которые", "который", "которая", "которых", "которой",
}


@dataclass
class Tokenizer:
    """Текст → list[str] лемм. См. модульный docstring про конвейер.

    Состояние объекта:
      morph     — MorphAnalyzer pymorphy3. Тяжёлый (≈30 МБ словаря в RAM),
                  создаём ОДИН раз на процесс и переиспользуем.
      stopwords — копия глобального списка; можно расширить под домен
                  для конкретного экземпляра, не трогая глобальную константу.
      min_len   — отрезаем токены короче этой длины. По умолчанию 2:
                  отсекает однобуквенные опечатки/мусор разметки.
      _cache    — словарь {raw_token: lemma}. pymorphy3 не самый быстрый,
                  а в реальных текстах слова часто повторяются — кэш
                  даёт прирост в разы при больших объёмах (наш случай).
    """
    morph: pymorphy3.MorphAnalyzer = field(default_factory=pymorphy3.MorphAnalyzer)
    stopwords: set[str] = field(default_factory=lambda: set(RU_STOPWORDS))
    min_len: int = 2
    _cache: dict[str, str] = field(default_factory=dict)

    def _lemma(self, token: str) -> str:
        cached = self._cache.get(token)
        if cached is not None: return cached
        parses = self.morph.parse(token)
        lemma = parses[0].normal_form if parses else token
        self._cache[token] = lemma
        return lemma

    def __call__(self, text: str) -> list[str]:
        out: list[str] = []
        for t in razdel_tokenize(text or ""):
            raw = t.text.lower()
            if not raw or raw in PUNCT: continue
            if not TOKEN_RE.match(raw): continue
            if len(raw) < self.min_len: continue
            lemma = self._lemma(raw)
            # Стоп-слова проверяем ПОСЛЕ лемматизации: «которые» в норме
            # «который», и именно «который» лежит в RU_STOPWORDS.
            # Дополнительная проверка длины — на случай, если лемма
            # оказалась короче исходного токена.
            if lemma in self.stopwords or len(lemma) < self.min_len: continue
            out.append(lemma)
        return out
